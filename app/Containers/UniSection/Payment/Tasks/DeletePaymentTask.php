<?php

namespace App\Containers\UniSection\Payment\Tasks;

use App\Containers\UniSection\Payment\Data\Repositories\PaymentRepository;
use App\Containers\UniSection\Payment\Events\PaymentDeletedEvent;
use App\Ship\Exceptions\DeleteResourceFailedException;
use App\Ship\Exceptions\NotFoundException;
use App\Ship\Parents\Tasks\Task as ParentTask;
use Illuminate\Database\Eloquent\ModelNotFoundException;

class DeletePaymentTask extends ParentTask
{
    public function __construct(
        protected readonly PaymentRepository $repository,
    ) {
    }

    /**
     * @throws DeleteResourceFailedException
     * @throws NotFoundException
     */
    public function run($id): int
    {
        try {
            $result = $this->repository->delete($id);
            PaymentDeletedEvent::dispatch($result);

            return $result;
        } catch (ModelNotFoundException) {
            throw new NotFoundException();
        } catch (\Exception) {
            throw new DeleteResourceFailedException();
        }
    }
}
